TITLE Adding Big Numbers
	
	.MODEL SMALL
	.STACK 64
	.DATA
	
	CT1 DW 0000
	CT2 DW 0000
	NUM1 DB 10 DUP('0'),'$'
	NUM2 DB 10 DUP('0'),'$'
	NUM3 DB 10 DUP('0'),'$'

	.CODE

MAIN PROC FAR
	
	MOV AX,@DATA
	MOV DS,AX

	LEA SI,NUM1
	LEA DI,NUM2
	CALL INPUT
	LEA BX,NUM3+9

A10:	MOV AL,[SI]
	MOV DL,[BX]
	SUB DL,30H
	ADD AL,DL
	ADD AL,[DI]
	MOV AH,00
	AAA
	ADD AL,30H
	MOV [BX],AL
	DEC BX
	DEC SI
	DEC DI
	MOV [BX],AH
	LOOP A10
	MOV AL,30H
	ADD [BX],AL
	
	MOV AH,09H
	LEA DX,NUM3
	INT 21H

	MOV AH,4CH
	INT 21H

MAIN ENDP

INPUT PROC NEAR

I10:

MOV AH,01H
INT 21H

CMP AL,'.'
JE I20

MOV [SI],AL
INC SI
INC CT1
JMP I10

I20:


MOV AH,01H
INT 21H

CMP AL,'.'
JE I30

MOV [DI],AL
INC DI
INC CT2
JMP I20

I30: 
DEC SI
DEC DI

MOV CX,CT1
CMP CX,CT2
JG I40
MOV CX,CT2

I40:
RET

INPUT ENDP
END MAIN
	