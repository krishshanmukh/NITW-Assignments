	TITLE Adding Big Numbers
	
	.MODEL SMALL
	.STACK 64
	.DATA
	
	NUM1 DB '650352'
	NUM2 DB '896325'
	NUM3 DB 10 DUP('0'),'$'

	.CODE

MAIN PROC FAR
	
	MOV AX,@DATA
	MOV DS,AX

	LEA SI,NUM1+5
	LEA DI,NUM2+5
	LEA BX,NUM3+9
	MOV CX,06

A10:	MOV AL,[SI]
	MOV DL,[BX]
	SUB DL,30H
	ADD AL,DL
	ADD AL,[DI]
	MOV AH,00
	AAA
	ADD AL,30H
	MOV [BX],AL
	DEC BX
	DEC SI
	DEC DI
	MOV [BX],AH
	;MOV AH,02H
	;MOV DL,[BX]
	;ADD DL,30H
	;INT 21H
	LOOP A10
	MOV AL,30H
	ADD [BX],AL
	
	MOV AH,09H
	LEA DX,NUM3
	INT 21H

	MOV AH,4CH
	INT 21H

MAIN ENDP
END MAIN
	