	;.386
	.MODEL SMALL
	.STACK 256
	.DATA

	STRING1 DB 'SHANMUKH$'
	STRING2 DB 20 DUP(' ')
	STRING3 DB 20 DUP(' ')
	STRING4 DB 'ANMUK'
	TEMP DB 05
	MSG DB 'FOUND$'
	N DW 0
	M DW 0

	.CODE

MAIN PROC FAR

	MOV AX,@DATA
	MOV DS,AX
	MOV ES,AX

	MOV CX,8
	LEA DI,STRING2
	LEA SI,STRING1
	REP MOVSB

	MOV AL,'$' ; COPY A STRING INTO ANOTHER MEMORY LOCATION
	MOV [DI],AL
	MOV AH,09H
	LEA DX,STRING2
	INT 21H;

	MOV AH,02H
	MOV DL,10D
	INT 21H

	MOV N,04 ; COPY FIRST N CHARS OF FIRST STRING TO SECOND
	MOV CX,N
	LEA SI,STRING1
	REP MOVSB

	MOV AL,'$'
	MOV [DI],AL
	MOV AH,09H
	LEA DX,STRING2
	INT 21H;

	MOV M,05 ; FIRST N OF STR1 + FIRST M OF STR2 INTO STR3
	MOV CX,N
	LEA DI,STRING3
	LEA SI,STRING2
	REP MOVSB

	MOV CX,M
	LEA SI,STRING2
	REP MOVSB

	MOV AH,02H
	MOV DL,0AH
	INT 21H

	MOV AL,'$'
	MOV [DI],AL
	MOV AH,09H
	LEA DX,STRING3
	INT 21H;
	CLD
	
	MOV AH,02H
	MOV DL,0AH
	INT 21H

	MOV AL,'A' ; SEARCH A CHARACTER N A STRING
	MOV CX,08
	LEA DI,STRING1
	REPNE SCASB
	JNZ EXIT

	MOV AH,09H
	LEA DX,MSG
	INT 21H

	LEA SI,STRING4

	MOV CX,08
	SUB CL,TEMP
	C20:
	MOV AL,[SI]
	LEA DI,STRING1
	REPNE SCASB
	JNZ EXIT
	PUSH SI
	PUSH CX
	PUSH DI
	MOV CL,TEMP
	DEC CX
	INC SI
	REPE CMPSB
	JZ C10
	POP DI
	POP CX
	POP SI
	JMP C20
	C10:
	POP DI
	POP CX
	POP SI
	MOV AH,09H
	DEC DI
	MOV DX,DI
	INT 21H


EXIT: MOV AH,4CH
	INT 21H;

MAIN ENDP

END MAIN