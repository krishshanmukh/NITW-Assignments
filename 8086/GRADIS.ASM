	.CODE
	MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	MOV ES,AX
	MOV AH,0FH
	INT 10H
	PUSH AX
	MOV AX,0012H
	INT 10H
	MOV AH,0BH
	MOV BX,0007H
	INT 10H
	CALL C10DISPLAY
	MOV AH,10H
	INT 16H
	POP AX
	MOV AH,00H
	INT 10H
	MOV AH,4CH
	INT 21H
	MAIN ENDP

	C10DISPLAY PROC NEAR
	PUSHA 
	MOV BX,00
	MOV CX,64
	MOV DX,70
	C20:
	MOV AH,0CH
	MOV AL,BL
	INT 10H
	INC CX
	CMP CX,576
	JNE C20
	INC BL
	INC DX
	CMP DX,280
	JNE C20
	POPA
	RET
	C10DISPLAY ENDP
	END MAIN