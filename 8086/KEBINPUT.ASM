.MODEL SMALL
.STACK 64
.DATA

PARA_LIST LABEL BYTE
MAX_LENGTH DB 20
ACT_LENGTH DB ?
KEB_NAME DB 20 DUP('0'),'$'

.CODE
MAIN PROC FAR

MOV AX,@DATA
MOV DS,AX
MOV AH,0AH
LEA DX,PARA_LIST
INT 21H
MOV BL,ACT_LENGTH
MOV BH,00
MOV KEB_NAME[BX],'$'
MOV AH,09H
LEA DX,KEB_NAME
INT 21H
MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN