	TITLE Scrolling

	.MODEL SMALL
	.STACK 256
	.DATA

	ROW1 DB 08
	ROW2 DB 16D
	COL1 DB 20D
	COL2 DB 56D
	ROWT DB 08
	COLT DB 20D
	.CODE

MAIN PROC FAR

	MOV AX,@DATA
	MOV DS,AX

	MOV AX,0600H
	MOV BH,10H
	MOV CX,0000H
	MOV DX,184FH
	INT 10H

	MOV AX,0600H
	CALL SETSCREEN

	CALL SETCURSOR

	M10:;LOOP FOR TAKING INPUT
	MOV AH,01H
	INT 21H
	INC COLT
	CMP COLT,57D;CHECK END OF LINE
	JNZ M20
	MOV COLT,20D
	INC ROWT
	CALL SETCURSOR
	M20:
	CMP AL,0DH; CHECK FOR ENTER
	JNZ M30
	INC ROWT
	MOV COLT,20D
	CALL SETCURSOR
	M30:; CHECK FOR BACKSPACE
	CMP AL,08H
	JNZ M50
	DEC COLT
	DEC COLT
	CMP COLT,19D; CHECK IF BACKSPACE+START OF LINE
	JNZ M40
	MOV COLT,55D
	DEC ROWT
	JMP M40
	DUMMY:
	JMP M10
	M40:
	CALL SETCURSOR
	MOV AH,02H
	MOV DL,' '
	INT 21H
	CALL SETCURSOR
	JMP M60; CHECK FOR UPSCROLLING
	M50: ;RET IF ESC
	MOV DH,ROW2
	CMP ROWT,DH; CHECK IF TO SCROLL
	JBE M60
	DEC ROWT
	CALL SETCURSOR
	MOV AX,0601H
	CALL SETSCREEN
	JMP M70
	M60:
	MOV DH,ROW1
	CMP ROWT,DH; CHECK IF TO SCROLL UP
	JAE M70
	INC ROWT
	CALL SETCURSOR
	MOV AX,0701H
	CALL SETSCREEN
	M70:
	CMP AL,1BH
	JNZ DUMMY
	

	MOV AH,4CH
	INT 21H

MAIN ENDP

SETSCREEN PROC NEAR
	
	MOV BH,35H
	MOV CH,ROW1
	MOV CL,COL1
	MOV DH,ROW2
	MOV DL,COL2
	INT 10H

	RET
SETSCREEN ENDP

SETCURSOR PROC NEAR

	MOV AH,02H;FN FOR SETTING CURSOR
	MOV BH,00
	MOV DH,ROWT
	MOV DL,COLT
	INT 10H

RET

SETCURSOR ENDP

END MAIN